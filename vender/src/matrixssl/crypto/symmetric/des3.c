/*
	Init the 3DES block cipher context for CBC-EDE mode.
	IV should point to 8 bytes of initialization vector
	Key should point to 24 bytes of data
*/
int32 psDes3Init(psCipherContext_t *ctx, unsigned char *IV,
				   unsigned char *key, uint32 keylen)
{
	int32 x, err;

	if (IV == NULL || key == NULL || ctx == NULL || keylen != DES3_KEY_LEN) {
		psTraceCrypto("Bad args to psDes3Init\n");
		return PS_ARG_FAIL;
	}
	memset(ctx, 0x0, sizeof(psCipherContext_t));
/*
	setup cipher
 */
	if ((err = psDes3InitKey(key, keylen, &ctx->des3.key)) != PS_SUCCESS) {
		return err;
	}
/*
	copy IV
 */
	ctx->des3.blocklen = DES3_IV_LEN;
	for (x = 0; x < ctx->des3.blocklen; x++) {
		ctx->des3.IV[x] = IV[x];
	}
	return PS_SUCCESS;
}

/******************************************************************************/
/*
	Encrypt a buffer using 3DES-EDE-CBC
	(Encrypt Decrypt Encrypt and Cipher Block Chaining)
	len must be a multiple of blockLen (8 bytes)
*/
int32 psDes3Encrypt(psCipherContext_t *ctx, unsigned char *pt,
					  unsigned char *ct, uint32 len)
{
	int32			x;
	uint32			i;
	unsigned char	tmp[MAXBLOCKSIZE];

	if (pt == NULL || ct == NULL || ctx == NULL || (len & 0x7) != 0) {
		psTraceCrypto("Bad args to psDes3Encrypt\n");
		return PS_ARG_FAIL;
	}

	/* is blocklen valid? */
	if (ctx->des3.blocklen < 0 || (ctx->des3.blocklen > 
			(int32)sizeof(ctx->des3.IV))) {
		psTraceCrypto("Blocklen test failed in psDes3Encrypt\n");	
		return PS_LIMIT_FAIL;
	}

	for (i = 0; i < len; i += ctx->des3.blocklen) {
		/* xor IV against plaintext */
		for (x = 0; x < ctx->des3.blocklen; x++) {
			tmp[x] = pt[x] ^ ctx->des3.IV[x];
		}
		/* encrypt */
		psDes3EncryptBlock(tmp, (unsigned char*)ct, &ctx->des3.key);

		/* store IV [ciphertext] for a future block */
		for (x = 0; x < ctx->des3.blocklen; x++) {
			ctx->des3.IV[x] = ct[x];
		}
		ct += ctx->des3.blocklen;
		pt += ctx->des3.blocklen;
	}

	memset(tmp, 0x0, sizeof(tmp));
	return len;
}

/******************************************************************************/
/*
	Decrypt a buffer using 3DES-EDE-CBC
	(Encrypt Decrypt Encrypt and Cipher Block Chaining)
	len must be a multiple of blockLen (8 bytes)
*/
int32 psDes3Decrypt(psCipherContext_t *ctx, unsigned char *ct,
					  unsigned char *pt, uint32 len)
{
	int32			x;
	uint32			i;
	unsigned char	tmp[MAXBLOCKSIZE], tmp2[MAXBLOCKSIZE];

	if (pt == NULL || ct == NULL || ctx == NULL || (len & 0x7) != 0) {
		psTraceCrypto("Bad args to psDes3Decrypt\n");
		return PS_ARG_FAIL;
	}

	/* is blocklen valid? */
	if (ctx->des3.blocklen < 0 || (ctx->des3.blocklen > 
			(int32)sizeof(ctx->des3.IV))) {
		psTraceCrypto("Blocklen test failed in psDes3Decrypt\n");	
		return PS_LIMIT_FAIL;
	}
	for (i = 0; i < len; i += ctx->des3.blocklen) {
		/* decrypt the block from ct into tmp */
		psDes3DecryptBlock(ct, tmp, &ctx->des3.key);
		/* xor IV against the plaintext of the previous step */
		for (x = 0; x < ctx->des3.blocklen; x++) { 
			/* copy CT in case ct == pt */
			tmp2[x] = ct[x]; 
			/* actually decrypt the byte */
			pt[x] = tmp[x] ^ ctx->des3.IV[x]; 
		}
		/* replace IV with this current ciphertext */
		for (x = 0; x < ctx->des3.blocklen; x++) {
			ctx->des3.IV[x] = tmp2[x];
		}
		ct += ctx->des3.blocklen;
		pt += ctx->des3.blocklen;
	}
	memset(tmp, 0x0, sizeof(tmp));
	memset(tmp2, 0x0, sizeof(tmp2));
	return len;
}


/*
	We don't validate DES keys against the following known weak keys.
	Astronomically small chances of randomly getting a weak key
	with 3DES. http://www.rsasecurity.com/rsalabs/faq/3-2-4.html

	http://www.itl.nist.gov/fipspubs/fip74.htm
	1.		E001E00lFl0lFl0l		01E001E00lFl0lFl
	2.		FElFFElFFEOEFEOE		1FFElFFEOEFEOEFE
	3.		E01FE01FF10EF10E		1FE01FEOOEF10EF1
	4.		01FE01FE01FE01FE		FE01FE01FE01FE01
	5.		011F011F0l0E010E		1F011F0l0E0l0E01
	6.		E0FEE0FEFlFEFlFE		FEE0FEE0FEFlFEF1
	7.		0101010101010101
	8.		FEFEFEFEFEFEFEFE
	9.		E0E0E0E0FlFlFlFl
	10.		lFlFlFlF0E0E0E0E
*/
int32 psDes3InitKey(const unsigned char *key, uint32 keylen, psDes3Key_t *skey)
{
	if (key == NULL || skey == NULL) {
		psTraceCrypto("Arg failure to psDes3InitKey\n");
		return PS_ARG_FAIL;
	}

	if (keylen != 24) {
		psTraceIntCrypto("Bad key size %d to psDes3InitKey\n", (int32)keylen);
		return CRYPT_INVALID_KEYSIZE;
	}

	deskey(key,	EN0, skey->ek[0]);
	deskey(key+8,	DE1, skey->ek[1]);
	deskey(key+16,	EN0, skey->ek[2]);

	deskey(key,		DE1, skey->dk[2]);
	deskey(key+8,	EN0, skey->dk[1]);
	deskey(key+16,	DE1, skey->dk[0]);

	return PS_SUCCESS;
}

void psDes3EncryptBlock(const unsigned char *pt, unsigned char *ct, 
							 psDes3Key_t *key)
{
	uint32 work[2];

	LOAD32H(work[0], pt+0);
	LOAD32H(work[1], pt+4);
	desfunc(work, key->ek[0]);
	desfunc(work, key->ek[1]);
	desfunc(work, key->ek[2]);
	STORE32H(work[0],ct+0);
	STORE32H(work[1],ct+4);
}

void psDes3DecryptBlock(const unsigned char *ct, unsigned char *pt, 
							 psDes3Key_t *key)
{
	uint32 work[2];

	LOAD32H(work[0], ct+0);
	LOAD32H(work[1], ct+4);
	desfunc(work, key->dk[0]);
	desfunc(work, key->dk[1]);
	desfunc(work, key->dk[2]);
	STORE32H(work[0],pt+0);
	STORE32H(work[1],pt+4);
}

