static void cookey(const uint32 *raw1, uint32 *keyout)
{
	uint32			*cook;
	const uint32	*raw0;
	uint32			dough[32];
	int32				i;
	
	cook = dough;
	for(i=0; i < 16; i++, raw1++) {
		raw0 = raw1++;
		*cook    = (*raw0 & 0x00fc0000L) << 6;
		*cook   |= (*raw0 & 0x00000fc0L) << 10;
		*cook   |= (*raw1 & 0x00fc0000L) >> 10;
		*cook++ |= (*raw1 & 0x00000fc0L) >> 6;
		*cook    = (*raw0 & 0x0003f000L) << 12;
		*cook   |= (*raw0 & 0x0000003fL) << 16;
		*cook   |= (*raw1 & 0x0003f000L) >> 4;
		*cook++ |= (*raw1 & 0x0000003fL);
	}
	
	psMemcpy(keyout, dough, sizeof dough);
#ifdef USE_BURN_STACK
	psBurnStack(sizeof(uint32 *) * 2 + sizeof(uint32)*32 + sizeof(int32));
#endif /* USE_BURN_STACK */
}

static void deskey(const unsigned char *key, int32 edf, uint32 *keyout)
{
	uint32				i, j, l, m, n, kn[32];
	unsigned char		pc1m[56], pcr[56];
	
	for (j=0; j < 56; j++) {
		l = (uint32)pc1[j];
		m = l & 7;
		pc1m[j] = (unsigned char)((key[l >> 3U] & bytebit[m]) == 
								  bytebit[m] ? 1 : 0);
	}
	
	for (i=0; i < 16; i++) {
		if (edf == DE1) {
			m = (15 - i) << 1;
		} else {
			m = i << 1;
		}
		n = m + 1;
		kn[m] = kn[n] = 0L;
		for (j=0; j < 28; j++) {
			l = j + (uint32)totrot[i];
			if (l < 28) {
				pcr[j] = pc1m[l];
			} else {
				pcr[j] = pc1m[l - 28];
			}
		}
		for (/*j = 28*/; j < 56; j++) {
			l = j + (uint32)totrot[i];
			if (l < 56) {
				pcr[j] = pc1m[l];
			} else {
				pcr[j] = pc1m[l - 28];
			}
		}
		for (j=0; j < 24; j++)  {
			if ((int32)pcr[(int32)pc2[j]] != 0) {
				kn[m] |= bigbyte[j];
			}
			if ((int32)pcr[(int32)pc2[j+24]] != 0) {
				kn[n] |= bigbyte[j];
			}
		}
	}
	cookey(kn, keyout);
#ifdef USE_BURN_STACK
	psBurnStack(sizeof(int32)*5 + sizeof(uint32)*32 +
		sizeof(unsigned char)*112);
#endif /* USE_BURN_STACK */
}

static void desfunc(uint32 *block, const uint32 *keys)
{
	uint32 work, right, leftt;
	int32 cur_round;
	
	leftt = block[0];
	right = block[1];
	
#if !defined(PS_3DES_IMPROVE_PERF_INCREASE_CODESIZE) || !defined(HAVE_NATIVE_INT64)
	work = ((leftt >> 4)  ^ right) & 0x0f0f0f0fL;
	right ^= work;
	leftt ^= (work << 4);
	
	work = ((leftt >> 16) ^ right) & 0x0000ffffL;
	right ^= work;
	leftt ^= (work << 16);
	
	work = ((right >> 2)  ^ leftt) & 0x33333333L;
	leftt ^= work;
	right ^= (work << 2);
	
	work = ((right >> 8)  ^ leftt) & 0x00ff00ffL;
	leftt ^= work;
	right ^= (work << 8);
	
	right = ROL(right, 1);
	work = (leftt ^ right) & 0xaaaaaaaaL;
	
	leftt ^= work;
	right ^= work;
	leftt = ROL(leftt, 1);
#else /* !PS_3DES_IMPROVE_PERF_INCREASE_CODESIZE && HAVE_NATIVE_INT64*/
	{
		uint64 tmp;
		tmp = des_ip[0][byte(leftt, 0)] ^
		des_ip[1][byte(leftt, 1)] ^
		des_ip[2][byte(leftt, 2)] ^
		des_ip[3][byte(leftt, 3)] ^
		des_ip[4][byte(right, 0)] ^
		des_ip[5][byte(right, 1)] ^
		des_ip[6][byte(right, 2)] ^
		des_ip[7][byte(right, 3)];
		leftt = (uint32)(tmp >> 32);
		right = (uint32)(tmp & 0xFFFFFFFFUL);
	}
#endif /* SMALL CODE */
	
	for (cur_round = 0; cur_round < 8; cur_round++) {
		work	= ROR(right, 4) ^ *keys++;
		leftt	^= SP7[work        & 0x3fL]
		^ SP5[(work >>  8) & 0x3fL]
		^ SP3[(work >> 16) & 0x3fL]
		^ SP1[(work >> 24) & 0x3fL];
		work	 = right ^ *keys++;
		leftt	^= SP8[ work        & 0x3fL]
		^  SP6[(work >>  8) & 0x3fL]
		^  SP4[(work >> 16) & 0x3fL]
		^  SP2[(work >> 24) & 0x3fL];
		
		work	= ROR(leftt, 4) ^ *keys++;
		right	^= SP7[ work        & 0x3fL]
		^  SP5[(work >>  8) & 0x3fL]
		^  SP3[(work >> 16) & 0x3fL]
		^  SP1[(work >> 24) & 0x3fL];
		work	 = leftt ^ *keys++;
		right	^= SP8[ work        & 0x3fL]
		^  SP6[(work >>  8) & 0x3fL]
		^  SP4[(work >> 16) & 0x3fL]
		^  SP2[(work >> 24) & 0x3fL];
	}
	
#if !defined(PS_3DES_IMPROVE_PERF_INCREASE_CODESIZE) || !defined(HAVE_NATIVE_INT64)
	right = ROR(right, 1);
	work = (leftt ^ right) & 0xaaaaaaaaL;
	leftt ^= work;
	right ^= work;
	leftt = ROR(leftt, 1);
	work = ((leftt >> 8) ^ right) & 0x00ff00ffL;
	right ^= work;
	leftt ^= (work << 8);
	
	work = ((leftt >> 2) ^ right) & 0x33333333L;
	right ^= work;
	leftt ^= (work << 2);
	work = ((right >> 16) ^ leftt) & 0x0000ffffL;
	leftt ^= work;
	right ^= (work << 16);
	work = ((right >> 4) ^ leftt) & 0x0f0f0f0fL;
	leftt ^= work;
	right ^= (work << 4);
#else /* !PS_3DES_IMPROVE_PERF_INCREASE_CODESIZE && HAVE_NATIVE_INT64 */
	{
		uint64 tmp;
		tmp = des_fp[0][byte(leftt, 0)] ^
		des_fp[1][byte(leftt, 1)] ^
		des_fp[2][byte(leftt, 2)] ^
		des_fp[3][byte(leftt, 3)] ^
		des_fp[4][byte(right, 0)] ^
		des_fp[5][byte(right, 1)] ^
		des_fp[6][byte(right, 2)] ^
		des_fp[7][byte(right, 3)];
		leftt = (uint32)(tmp >> 32);
		right = (uint32)(tmp & 0xFFFFFFFFUL);
	}
#endif /* PS_3DES_IMPROVE_PERF_INCREASE_CODESIZE */
	
	block[0] = right;
	block[1] = leftt;
#ifdef USE_BURN_STACK
	psBurnStack(sizeof(uint32) * 4 + sizeof(int32));
#endif /* USE_BURN_STACK */
}

#endif /* USE_3DES || USE_DES */
